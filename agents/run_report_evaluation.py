"""Driver for the report_evaluation skill.

Emits ONE prompt per (agent, symbol, model) run folder under
results/report_generation/. Optionally targets a specific folder.
"""

from __future__ import annotations

import argparse
import os
import re
import sys
from pathlib import Path

SUPPORTED_SYMBOLS = {"AAPL", "ADBE", "AMZN", "GOOGL", "META", "MSFT", "NVDA", "TSLA"}

RUNDIR_PATTERN = re.compile(
    r"^(?P<agent>.+)_report_generation_(?P<symbol>[A-Z]+)_(?P<model>.+)$"
)

PROMPT_TEMPLATE = """\
Evaluate the weekly equity reports for {symbol} generated by the {agent}
agent using {model}.

Report directory: {report_dir}
Output directory: {output_dir}

agent_name = {eval_agent}
model = {eval_model}
"""


def _find_runs(report_parent: Path, symbol: str | None, agent: str | None, model: str | None):
    runs = []
    for sub in sorted(report_parent.iterdir()) if report_parent.is_dir() else []:
        if not sub.is_dir():
            continue
        m = RUNDIR_PATTERN.match(sub.name)
        if not m:
            continue
        if symbol and m.group("symbol") != symbol:
            continue
        if agent and m.group("agent") != agent:
            continue
        if model and m.group("model") != model:
            continue
        runs.append((sub, m.group("agent"), m.group("symbol"), m.group("model")))
    return runs


def main() -> int:
    p = argparse.ArgumentParser(description=__doc__)
    p.add_argument("--report-parent", default="results/report_generation",
                   help="Parent directory containing per-run subfolders")
    p.add_argument("--output-dir", default="results/report_evaluation")
    p.add_argument("--symbol", default=None, help="Filter runs by symbol (one of 8)")
    p.add_argument("--agent", default=None, help="Filter runs by agent name")
    p.add_argument("--model", default=None, help="Filter runs by model identifier")
    p.add_argument("--eval-agent", default="claude-code")
    p.add_argument("--eval-model", default="claude-sonnet-4-6")
    args = p.parse_args()

    if args.symbol and args.symbol.upper() not in SUPPORTED_SYMBOLS:
        raise SystemExit(
            f"Unsupported --symbol {args.symbol!r}. Must be one of: {sorted(SUPPORTED_SYMBOLS)}"
        )

    runs = _find_runs(
        Path(args.report_parent),
        args.symbol.upper() if args.symbol else None,
        args.agent,
        args.model,
    )
    if not runs:
        print(
            f"[{Path(__file__).name}] no matching runs under {args.report_parent}",
            file=sys.stderr,
        )
        return 1

    for rundir, run_agent, run_symbol, run_model in runs:
        print("=" * 72)
        print(PROMPT_TEMPLATE.format(
            symbol=run_symbol,
            agent=run_agent,
            model=run_model,
            report_dir=str(rundir),
            output_dir=args.output_dir,
            eval_agent=args.eval_agent,
            eval_model=args.eval_model,
        ).rstrip())
    print("=" * 72, file=sys.stderr)
    print(
        f"[{Path(__file__).name}] emitted {len(runs)} evaluation prompts",
        file=sys.stderr,
    )
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
